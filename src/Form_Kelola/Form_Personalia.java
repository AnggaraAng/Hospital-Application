    public void tampil_tabel_personalia() {
        Object[] baris = {"idPegawai", "namaLengkap", "alamat", "jenisKlamin", "tanggalLahir", "noTelp", "departemen", "gaji"};
        tabmode = new DefaultTableModel(null, baris);
        tabel_personalia.setModel(tabmode);
        con = connection.getConnection();
        try {
            String sql = "select * from tabel_personalia order by idPegawai asc";
            java.sql.Statement stat = con.createStatement();
            java.sql.ResultSet rslt = stat.executeQuery(sql);
            while (rslt.next()) {
                String idPegawai = rslt.getString("idPegawai");
                String namaLengkap = rslt.getString("namaLengkap");
                String alamat = rslt.getString("alamat");
                String jenisKlamin = rslt.getString("jenisKlamin");
                String tanggalLahir = rslt.getString("tanggalLahir");
                String noTelp = rslt.getString("noTelp");
                String departemen = rslt.getString("departemen");
                String gaji = rslt.getString("gaji");
                String[] data = {idPegawai, namaLengkap, alamat, jenisKlamin, tanggalLahir, noTelp, departemen, gaji};
                tabmode.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Menampilkan data GAGAL", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        }
    }

