    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        String idPegawai = "", namaLengkap = "", alamat = "", jenisKlamin = "", tanggalLahir = "", noTelp = "", departemen = "", gaji = "";

        idPegawai = text_id.getText();
        namaLengkap = text_nm.getText();
        alamat = text_alt.getText();
        jenisKlamin = text_jk.getText();
        tanggalLahir = text_tl.getText();
        noTelp = text_telp.getText();
        departemen = text_dept.getText();
        gaji = text_gaji.getText();

        con = connection.getConnection();
        try {
            String sql = "insert into tabel_personalia values (?,?,?,?,?,?,?,?)";
            PreparedStatement stat = (PreparedStatement) con.prepareStatement(sql);

            stat.setString(1, idPegawai);
            stat.setString(2, namaLengkap);
            stat.setString(3, alamat);
            stat.setString(4, jenisKlamin);
            stat.setString(5, tanggalLahir);
            stat.setString(6, noTelp);
            stat.setString(7, departemen);
            stat.setString(8, gaji);
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Menyimpan data BERHASIL", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            reset();
            tampil_tabel_personalia();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Menyimpan data GAGAL", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_saveBtnActionPerformed

    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        con = connection.getConnection();
        String idPegawai = "", namaLengkap = "", alamat = "", jenisKlamin = "", tanggalLahir = "", noTelp = "", departemen = "", gaji = "";

        idPegawai = text_id.getText();
        namaLengkap = text_nm.getText();
        alamat = text_alt.getText();
        jenisKlamin = text_jk.getText();
        tanggalLahir = text_tl.getText();
        noTelp = text_telp.getText();
        departemen = text_dept.getText();
        gaji = text_gaji.getText();

        if (namaLengkap == null) {
            JOptionPane.showMessageDialog(null, "Silahkan isi Nama Lengkap anda", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        } else {
            String sql = "update tabel_personalia set namaLengkap=?, alamat=?, jenisKlamin=?, tanggalLahir=?, noTelp=?, departemen=?, gaji=? where idPegawai='" + idPegawai + "'";
            java.sql.PreparedStatement stat = null;
            try {
                stat = con.prepareStatement(sql);
                stat.setString(1, namaLengkap);
                stat.setString(2, alamat);
                stat.setString(3, jenisKlamin);
                stat.setString(4, tanggalLahir);
                stat.setString(5, noTelp);
                stat.setString(6, departemen);
                stat.setString(7, gaji);
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data BERHASIL di Ubah");
                tampil_tabel_personalia();
                reset();
            } catch (SQLException se) {
                JOptionPane.showMessageDialog(null, "Data GAGAL di Ubah");
            }
        }
    }//GEN-LAST:event_updateBtnActionPerformed

