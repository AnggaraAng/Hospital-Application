    public void pencarian_personalia() {
        String cari = text_search.getText();

        Object[] Baris = {"idPegawai", "namaLengkap", "alamat", "jenisKlamin", "tanggalLahir", "noTelp", "departemen", "gaji"};
        tabmode = new DefaultTableModel(null, Baris);
        tabel_personalia.setModel(tabmode);

        con = connection.getConnection();

        try {
            String sql = "Select * from tabel_personalia where "
                    + "idPegawai like '%" + cari + "%' "
                    + "OR namaLengkap like '%" + cari + "%' "
                    + "OR alamat like '%" + cari + "%' "
                    + "OR jenisKlamin like '%" + cari + "%' "
                    + "OR tanggalLahir like '%" + cari + "%' "
                    + "OR noTelp like '%" + cari + "%' "
                    + "OR departemen like '%" + cari + "%' "
                    + "OR gaji like '%" + cari + "%' "
                    + "order by idPegawai asc";
            java.sql.Statement stmt = con.createStatement();
            java.sql.ResultSet rslt = stmt.executeQuery(sql);
            while (rslt.next()) {
                String idPegawai = rslt.getString("idPegawai");
                String namaLengkap = rslt.getString("namaLengkap");
                String alamat = rslt.getString("alamat");
                String jenisKlamin = rslt.getString("jenisKlamin");
                String tanggalLahir = rslt.getString("tanggalLahir");
                String noTelp = rslt.getString("noTelp");
                String departemen = rslt.getString("departemen");
                String gaji = rslt.getString("gaji");

                String[] dataField = {idPegawai, namaLengkap, alamat, jenisKlamin, tanggalLahir, noTelp, departemen, gaji};
                tabmode.addRow(dataField);
            }
        } catch (Exception ex) {
        }
    }

