    public void pencarian_farmasi() {
        String cari = text_search.getText();

        Object[] Baris = {"idProduk", "namaProduk", "kategoriObat", "tanggalMasuk", "kadaluarsa", "jumlahStock", "hargaProduk"};
        tabmode = new DefaultTableModel(null, Baris);
        tabel_farmasi.setModel(tabmode);

        con = connection.getConnection();

        try {
            String sql = "Select * from tabel_farmasi where "
                    + "idProduk like '%" + cari + "%' "
                    + "OR namaProduk like '%" + cari + "%' "
                    + "OR kategoriObat like '%" + cari + "%' "
                    + "OR tanggalMasuk like '%" + cari + "%' "
                    + "OR kadaluarsa like '%" + cari + "%' "
                    + "OR jumlahStock like '%" + cari + "%' "
                    + "OR hargaProduk like '%" + cari + "%' "
                    + "order by idProduk asc";
            java.sql.Statement stmt = con.createStatement();
            java.sql.ResultSet rslt = stmt.executeQuery(sql);
            while (rslt.next()) {
                String idProduk = rslt.getString("idProduk");
                String namaProduk = rslt.getString("namaProduk");
                String kategoriObat = rslt.getString("kategoriObat");
                String tanggalMasuk = rslt.getString("tanggalMasuk");
                String kadaluarsa = rslt.getString("kadaluarsa");
                String jumlahStock = rslt.getString("jumlahStock");
                String hargaProduk = rslt.getString("hargaProduk");
       

                String[] dataField = {idProduk, namaProduk, kategoriObat, tanggalMasuk, kadaluarsa, jumlahStock, hargaProduk};
                tabmode.addRow(dataField);
            }
        } catch (Exception ex) {
        }
    }

