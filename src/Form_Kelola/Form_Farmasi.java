    public void tampil_tabel_farmasi() {
        Object[] baris = {"idProduk", "namaProduk", "kategoriObat", "tanggalMasuk", "kadaluarsa", "jumlahStock", "hargaProduk"};
        tabmode = new DefaultTableModel(null, baris);
        tabel_farmasi.setModel(tabmode);
        con = connection.getConnection();
        try {
            String sql = "select * from tabel_farmasi order by idProduk asc";
            java.sql.Statement stat = con.createStatement();
            java.sql.ResultSet rslt = stat.executeQuery(sql);
            while (rslt.next()) {
                String idProduk = rslt.getString("idProduk");
                String namaProduk = rslt.getString("namaProduk");
                String kategoriObat = rslt.getString("kategoriObat");
                String tanggalMasuk = rslt.getString("tanggalMasuk");
                String kadaluarsa = rslt.getString("kadaluarsa");
                String jumlahStock = rslt.getString("jumlahStock");
                String hargaProduk = rslt.getString("hargaProduk");
            
                String[] data = {idProduk, namaProduk, kategoriObat, tanggalMasuk, kadaluarsa, jumlahStock, hargaProduk};
                tabmode.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Menampilkan data GAGAL", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        }
    }
