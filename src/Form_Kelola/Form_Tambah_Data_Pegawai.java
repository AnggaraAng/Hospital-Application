    public void tampil_tabel_karyawan() {
        Object[] baris = {"username", "namaKaryawan", "departemen", "email", "hakAkses", "password", "retypePassword"};
        tabmode = new DefaultTableModel(null, baris);
        tabel_karyawan.setModel(tabmode);
        con = connection.getConnection();
        try {
            String sql = "select * from tabel_pegawai order by username asc";
            java.sql.Statement stat = con.createStatement();
            java.sql.ResultSet rslt = stat.executeQuery(sql);
            while (rslt.next()) {
                String username = rslt.getString("username");
                String namaKaryawan = rslt.getString("namaKaryawan");
                String departemen = rslt.getString("departemen");
                String email = rslt.getString("email");
                String hakAkses = rslt.getString("hakAkses");
                String password = rslt.getString("password");
                String retypePassword = rslt.getString("retypePassword");
                String[] data = {username, namaKaryawan, departemen, email, hakAkses, password, retypePassword};
                tabmode.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Menampilkan data GAGAL", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        }
    }
