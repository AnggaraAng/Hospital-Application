    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        String username = "", namaKaryawan = "", departemen = "", email = "", hakAkses = "", password = "", retypePassword = "";

        username = text_username.getText();
        namaKaryawan = text_nm.getText();
        departemen = text_dept.getText();
        email = text_email.getText();
        hakAkses = (String) combo_hak_akses.getSelectedItem();
        password = text_password.getText();
        retypePassword = text_password2.getText();

        con = connection.getConnection();
        try {
            String sql = "insert into tabel_pegawai values (?,?,?,?,?,?,?)";
            PreparedStatement stat = (PreparedStatement) con.prepareStatement(sql);

            stat.setString(1, username);
            stat.setString(2, namaKaryawan);
            stat.setString(3, departemen);
            stat.setString(4, email);
            stat.setString(5, hakAkses);
            stat.setString(6, password);
            stat.setString(7, retypePassword);
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Menyimpan data BERHASIL", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            reset();
            tampil_tabel_karyawan();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Menyimpan data GAGAL", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_saveBtnActionPerformed

    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        con = connection.getConnection();
        String username = "", namaKaryawan = "", departemen = "", email = "", hakAkses = "", password = "", retypePassword = "";

        username = text_username.getText();
        namaKaryawan = text_nm.getText();
        departemen = text_dept.getText();
        email = text_email.getText();
        hakAkses = (String) combo_hak_akses.getSelectedItem();
        password = text_password.getText();
        retypePassword = text_password2.getText();

        if (email == null) {
            JOptionPane.showMessageDialog(null, "Silahkan isi E-mail anda", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        } else {
            String sql = "update tabel_pegawai set namaKaryawan=?, departemen=?, email=?, hakAkses=?, password=?, retypePassword=? where username='" + username + "'";
            java.sql.PreparedStatement stat = null;
            try {
                stat = con.prepareStatement(sql);
                stat.setString(1, namaKaryawan);
                stat.setString(2, departemen);
                stat.setString(3, email);
                stat.setString(4, hakAkses);
                stat.setString(5, password);
                stat.setString(6, retypePassword);
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data BERHASIL di Ubah");
                tampil_tabel_karyawan();
                reset();
            } catch (SQLException se) {
                JOptionPane.showMessageDialog(null, "Data GAGAL di Ubah");
            }
        }
    }//GEN-LAST:event_updateBtnActionPerformed

