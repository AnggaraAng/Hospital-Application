public void pencarian_karyawan() {
        String cari = text_search.getText();

        Object[] Baris = {"username", "namaKaryawan", "departemen", "email", "hakAkses", "password", "retypePassword"};
        tabmode = new DefaultTableModel(null, Baris);
        tabel_karyawan.setModel(tabmode);

        con = connection.getConnection();

        try {
            String sql = "Select * from tabel_pegawai where "
                    + "username like '%" + cari + "%' "
                    + "OR namaKaryawan like '%" + cari + "%' "
                    + "OR departemen like '%" + cari + "%' "
                    + "OR email like '%" + cari + "%' "
                    + "OR hakAkses like '%" + cari + "%' "
                    + "OR password like '%" + cari + "%' "
                    + "OR retypePassword like '%" + cari + "%' "
                    + "order by username asc";
            java.sql.Statement stmt = con.createStatement();
            java.sql.ResultSet rslt = stmt.executeQuery(sql);
            while (rslt.next()) {
                String username = rslt.getString("username");
                String namaKaryawan = rslt.getString("namaKaryawan");
                String departemen = rslt.getString("departemen");
                String email = rslt.getString("email");
                String hakAkses = rslt.getString("hakAkses");
                String password = rslt.getString("password");
                String retypePassword = rslt.getString("retypePassword");

                String[] dataField = {username, namaKaryawan, departemen, email, hakAkses, password, retypePassword};
                tabmode.addRow(dataField);
            }
        } catch (Exception ex) {
        }
    }

