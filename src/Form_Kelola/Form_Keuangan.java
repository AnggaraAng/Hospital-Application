    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        String idTransaksi = "", namaTransaksi = "", jenisTransaksi = "", totalPemasukan = "", totalPengeluaran = "", tanggalTransaksi = "";

        idTransaksi = text_id.getText();
        namaTransaksi = text_nm.getText();
        jenisTransaksi = text_jenis.getText();
        totalPemasukan = text_pemasukan.getText();
        totalPengeluaran = text_pengeluaran.getText();
        tanggalTransaksi = text_tgl.getText();

        con = connection.getConnection();
        try {
            String sql = "insert into tabel_keuangan values (?,?,?,?,?,?)";
            PreparedStatement stat = (PreparedStatement) con.prepareStatement(sql);

            stat.setString(1, idTransaksi);
            stat.setString(2, namaTransaksi);
            stat.setString(3, jenisTransaksi);
            stat.setString(4, totalPemasukan);
            stat.setString(5, totalPengeluaran);
            stat.setString(6, tanggalTransaksi);
            
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Menyimpan data BERHASIL", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            reset();
            tampil_tabel_keuangan();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Menyimpan data GAGAL", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_saveBtnActionPerformed

    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        con = connection.getConnection();
        String idTransaksi = "", namaTransaksi = "", jenisTransaksi = "", totalPemasukan = "", totalPengeluaran = "", tanggalTransaksi = "";

        idTransaksi = text_id.getText();
        namaTransaksi = text_nm.getText();
        jenisTransaksi = text_jenis.getText();
        totalPemasukan = text_pemasukan.getText();
        totalPengeluaran = text_pengeluaran.getText();
        tanggalTransaksi = text_tgl.getText();
       
       

        if (namaTransaksi == null) {
            JOptionPane.showMessageDialog(null, "Silahkan isi Nama Produk anda", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        } else {
            String sql = "update tabel_keuangan set namaTransaksi=?, jenisTransaksi=?, totalPemasukan=?, totalPengeluaran=?, tanggalTransaksi=? where idTransaksi='" + idTransaksi + "'";
            java.sql.PreparedStatement stat = null;
            try {
                stat = con.prepareStatement(sql);
                stat.setString(1, namaTransaksi);
                stat.setString(2, jenisTransaksi);
                stat.setString(3, totalPemasukan);
                stat.setString(4, totalPengeluaran);
                stat.setString(5, tanggalTransaksi);
               
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data BERHASIL di Ubah");
                tampil_tabel_keuangan();
                reset();
            } catch (SQLException se) {
                JOptionPane.showMessageDialog(null, "Data GAGAL di Ubah");
            }
        }
    }//GEN-LAST:event_updateBtnActionPerformed

