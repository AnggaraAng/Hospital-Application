    public void tampil_tabel_keuangan() {
        Object[] baris = {"idTransaksi", "namaTransaksi", "jenisTransaksi", "totalPemasukan", "totalPengeluaran", "tanggalTransaksi"};
        tabmode = new DefaultTableModel(null, baris);
        tabel_keuangan.setModel(tabmode);
        con = connection.getConnection();
        try {
            String sql = "select * from tabel_keuangan order by idTransaksi asc";
            java.sql.Statement stat = con.createStatement();
            java.sql.ResultSet rslt = stat.executeQuery(sql);
            while (rslt.next()) {
                String idTransaksi = rslt.getString("idTransaksi");
                String namaTransaksi = rslt.getString("namaTransaksi");
                String jenisTransaksi = rslt.getString("jenisTransaksi");
                String totalPemasukan = rslt.getString("totalPemasukan");
                String totalPengeluaran = rslt.getString("totalPengeluaran");
                String tanggalTransaksi = rslt.getString("tanggalTransaksi");
               
            
                String[] data = {idTransaksi, namaTransaksi, jenisTransaksi, totalPemasukan, totalPengeluaran, tanggalTransaksi};
                tabmode.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Menampilkan data GAGAL", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        }
    }
